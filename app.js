const setLanguage = (lang) => {
  const data = translations[lang] || translations.en;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.body.classList.toggle("rtl", lang === "ar");

  // Support text, HTML and placeholder translations
  document.querySelectorAll("[data-i18n], [data-i18n-html], [data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n") || el.getAttribute("data-i18n-html") || el.getAttribute("data-i18n-placeholder");
    if (!key) return;
    const val = data[key];
    if (!val) return;
    if (el.hasAttribute("data-i18n-html")) {
      el.innerHTML = val;
    } else if (el.hasAttribute("data-i18n-placeholder")) {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });

  document.querySelectorAll(".lang-toggle button").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });

  const floatingWhatsApp = document.getElementById("floating-whatsapp");
  if (floatingWhatsApp) {
    floatingWhatsApp.href = `https://wa.me/201099259725?text=${encodeURIComponent(data.whatsapp_message)}`;
    floatingWhatsApp.setAttribute("aria-label", data.whatsapp_label);
    floatingWhatsApp.setAttribute("title", data.whatsapp_label);
  }

  localStorage.setItem("lang", lang);
};

const initLangToggle = () => {
  const saved = localStorage.getItem("lang") || "en";
  setLanguage(saved);

  document.querySelectorAll(".lang-toggle button").forEach((btn) => {
    btn.addEventListener("click", () => setLanguage(btn.dataset.lang));
  });
};

const initReveal = () => {
  const revealEls = document.querySelectorAll(".reveal, .stagger");
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.2 }
  );

  revealEls.forEach((el) => observer.observe(el));
};

const initCursor = () => {
  const dot = document.getElementById("cursor-dot");
  const ring = document.getElementById("cursor-ring");
  if (!dot || !ring) return;

  if (window.matchMedia("(hover: none), (pointer: coarse)").matches) return;

  document.body.classList.add("cursor-active");

  let mx = 0;
  let my = 0;
  let rx = 0;
  let ry = 0;

  document.addEventListener("mousemove", (event) => {
    mx = event.clientX;
    my = event.clientY;
  });

  const loop = () => {
    rx += (mx - rx) * 0.14;
    ry += (my - ry) * 0.14;
    dot.style.cssText = `left:${mx - 3}px;top:${my - 3}px`;
    ring.style.cssText = `left:${rx - 16}px;top:${ry - 16}px`;
    requestAnimationFrame(loop);
  };

  loop();
};

const countUp = (el, target, duration = 1600) => {
  let start = 0;
  const step = (target / duration) * 16;
  const counter = el.querySelector(".count");
  if (!counter) return;

  const tick = () => {
    start = Math.min(start + step, target);
    counter.textContent = Math.round(start);
    if (start < target) requestAnimationFrame(tick);
  };

  tick();
};

const initCountUp = () => {
  const statsSections = document.querySelectorAll(".about-stats");
  if (!statsSections.length) return;

  const statsObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.querySelectorAll("[data-target]").forEach((el) => {
          countUp(el, Number(el.dataset.target));
        });
        statsObserver.unobserve(entry.target);
      });
    },
    { threshold: 0.3 }
  );

  statsSections.forEach((section) => statsObserver.observe(section));
};

